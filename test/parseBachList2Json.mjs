function parse(value) {
  // we need to insert commas at right places:
  for (let i = 0; i < value.length - 1; i++) {
    if (value[i] != '[' && value[i] != ',' && value[i + 1] !== ']') {
      value.splice(i + 1, 0, ',');
    }
  }

  value = `[${value.join('')}]`;

  console.log(value);
  console.log(JSON5.parse(value));
}

